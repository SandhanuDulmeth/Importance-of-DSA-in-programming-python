# Step 1: Define a list of fruits
fruits = ['apple', 'banana', 'cherry', 'date']

# Step 2: Access the first element (index 0)
first_fruit = fruits[0]
print("First fruit:", first_fruit)  # Output: apple

# Step 3: Access the last element using negative indexing (index -1)
last_fruit = fruits[-1]
print("Last fruit:", last_fruit)    # Output: date

# Step 4: Access a middle element (e.g., index 2)
middle_fruit = fruits[2]
print("Middle fruit:", middle_fruit)  # Output: cherry