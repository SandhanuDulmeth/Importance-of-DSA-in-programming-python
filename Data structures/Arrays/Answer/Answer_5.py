# Step 1: Create a list of animal names
animals = ['cat', 'dog', 'rabbit', 'parrot']

# Step 2: Use a for loop to iterate through the list
print("Animals in the list:")
for animal in animals:
    # Step 3: Print the current element
    print(animal)
    
    # Step 4 (Optional): Print the animal's name in uppercase
    print("In uppercase:", animal.upper())
